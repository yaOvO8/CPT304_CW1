function getCurrentLanguage() {
  const saved = localStorage.getItem(bizTrackLanguageKey);
  return bizTrackTranslations[saved] ? saved : bizTrackDefaultLanguage;
}

function getTranslationValue(key, language) {
  const dictionary = bizTrackTranslations[language] || bizTrackTranslations.en;
  return dictionary[key] || bizTrackTranslations.en[key] || key;
}

function interpolateTranslation(template, params = {}) {
  return template.replace(/\{(\w+)\}/g, function (_, key) {
    return params[key] ?? "";
  });
}

function t(key, params = {}) {
  return interpolateTranslation(templateFor(key), params);
}

function templateFor(key) {
  return getTranslationValue(key, getCurrentLanguage());
}

function setLanguage(language) {
  const nextLanguage = bizTrackTranslations[language] ? language : bizTrackDefaultLanguage;
  localStorage.setItem(bizTrackLanguageKey, nextLanguage);
  applyTranslations();
  document.dispatchEvent(new CustomEvent("biztrack:languagechange", { detail: { language: nextLanguage } }));
}

function formatCurrency(amount) {
  return new Intl.NumberFormat(bizTrackLocaleMap[getCurrentLanguage()] || "en-US", {
    style: "currency",
    currency: "USD",
    minimumFractionDigits: 2,
  }).format(amount);
}

function translateDynamicValue(type, value) {
  const map = bizTrackValueKeyMaps[type];

  if (!map || !map[value]) {
    return value;
  }

  return t(map[value]);
}

function applyTranslations() {
  const language = getCurrentLanguage();
  document.documentElement.lang = language;

  const pageTitleKey = document.body ? document.body.dataset.pageTitleKey : "";
  if (pageTitleKey) {
    document.title = `${t(pageTitleKey)} - BizTrack`;
  }

  document.querySelectorAll("[data-i18n]").forEach(function (element) {
    element.textContent = t(element.dataset.i18n);
  });

  document.querySelectorAll("[data-i18n-html]").forEach(function (element) {
    element.innerHTML = t(element.dataset.i18nHtml);
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach(function (element) {
    element.setAttribute("placeholder", t(element.dataset.i18nPlaceholder));
  });

  document.querySelectorAll("[data-i18n-aria-label]").forEach(function (element) {
    element.setAttribute("aria-label", t(element.dataset.i18nAriaLabel));
  });

  document.querySelectorAll("[data-i18n-label]").forEach(function (element) {
    element.setAttribute("label", t(element.dataset.i18nLabel));
  });

  const languageCode = document.getElementById("current-language-code");
  if (languageCode) {
    languageCode.textContent = language.toUpperCase();
  }

  document.querySelectorAll("[data-language-option]").forEach(function (button) {
    const isActive = button.dataset.languageOption === language;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });
}

function initializeLanguageSwitcher() {
  const toggle = document.getElementById("language-toggle");
  const menu = document.getElementById("language-menu");

  if (!toggle || !menu) {
    applyTranslations();
    return;
  }

  menu.querySelectorAll("[data-language-option]").forEach(function (button) {
    button.addEventListener("click", function () {
      setLanguage(button.dataset.languageOption);
      menu.classList.remove("is-open");
      toggle.setAttribute("aria-expanded", "false");
    });
  });

  toggle.addEventListener("click", function () {
    const willOpen = !menu.classList.contains("is-open");
    menu.classList.toggle("is-open", willOpen);
    toggle.setAttribute("aria-expanded", String(willOpen));
  });

  document.addEventListener("click", function (event) {
    if (!menu.contains(event.target) && !toggle.contains(event.target)) {
      menu.classList.remove("is-open");
      toggle.setAttribute("aria-expanded", "false");
    }
  });

  applyTranslations();
}

document.addEventListener("DOMContentLoaded", initializeLanguageSwitcher);

